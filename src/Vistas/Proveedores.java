    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if (!this.txtBuscar.getText().isEmpty()) {
            ArrayList<Proveedor> prove = this.con.getFiltroProveedor(this.nombresColumnas[this.comboBoxFiltro.getSelectedIndex()], this.txtBuscar.getText());
            this.tableModel.setRowCount(0);
            tableModel = (DefaultTableModel) tablaProveedores.getModel();
            this.proveedor = this.con.getFiltroProveedor(this.nombresColumnas[this.comboBoxFiltro.getSelectedIndex()], this.txtBuscar.getText());
            for (int i = 0; i < prove.size(); i++) {
                tableModel.addRow(new Object[]{this.proveedor.get(i).getId(), this.proveedor.get(i).getNombre(), this.proveedor.get(i).getApellido(), this.proveedor.get(i).getTelefono(), this.proveedor.get(i).getCorreo(), this.proveedor.get(i).getEmpresa(), this.proveedor.get(i).getDireccion(), this.proveedor.get(i).getRtn()});
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

