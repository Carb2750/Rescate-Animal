    private void setHeaders() {
        String[] tableTitles = new String[]{"ID", "Nombre", "Apellido", "Telefono", "Correo Electronico", "Empresa", "Direccion", "RTN"};
        JTableHeader header = tablaProveedores.getTableHeader();
        TableColumnModel headerModel = header.getColumnModel();

        Integer posHeader = 0;
        for (String title : tableTitles) {
            headerModel.getColumn(posHeader).setHeaderValue(title);
            posHeader++;
        }
    }
    
    private void getProveedores() {
        tableModel = (DefaultTableModel) tablaProveedores.getModel();
        this.proveedor = this.con.getProveedores();
        for (int i = 0; i < this.proveedor.size(); i++) {
            tableModel.addRow(new Object[]{this.proveedor.get(i).getId(), this.proveedor.get(i).getNombre(), this.proveedor.get(i).getApellido(), this.proveedor.get(i).getTelefono(), this.proveedor.get(i).getCorreo(), this.proveedor.get(i).getEmpresa(), this.proveedor.get(i).getDireccion(), this.proveedor.get(i).getRtn()});
        }
    }

