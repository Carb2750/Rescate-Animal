    private void BtnAgregarProActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarProActionPerformed
        Proveedor pro = new Proveedor("", this.txtNombre.getText(), this.txtApellido.getText(), this.txtTelefono.getText(), this.txtCorreo.getText(), this.txtEmpresa.getText(), this.txtDireccion.getText(), this.txtRtn.getText());
        this.con.insertProveedor(pro);
        tableModel.setRowCount(0);
        this.getProveedores();
        txtNombre.setText(null);
        txtApellido.setText(null);
        txtTelefono.setText(null);
        txtCorreo.setText(null);
        txtDireccion.setText(null);
        txtEmpresa.setText(null);
        txtRtn.setText(null);
    }//GEN-LAST:event_BtnAgregarProActionPerformed

